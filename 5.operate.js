console.log(123 == 123);
console.log(123 == '123');

// 비교연산자 === : 타입이 같으면 true, 다르면 false
console.log(123 === '123');  
console.log(123 === 123);

console.log(123 != 123);
console.log(123 != '123');

// !== : 타입이 다르면 true, 같으면 false
console.log("123 !== '123'" + 123 !== '123');
console.log(123 !== 123);
